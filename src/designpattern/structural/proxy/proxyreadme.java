public class proxyreadme {
  /*  Let’s go step by step —
  Proxy Design Pattern is very important in Java/Spring interviews and
  it is often confused with Decorator.

🧠 Proxy Design Pattern (Simple Meaning)

    Proxy means a substitute / placeholder for another object that controls access to it.

    Instead of directly calling the real object, you call a proxy object, and the proxy decides:

    whether to allow access
    what extra work to do before/after
    whether to delay creation (lazy loading)
🏠 Real-life analogy

    Think of a credit card payment system:

    You don’t directly talk to the bank.

    You go through:

    Payment gateway (Proxy)

    It:

    checks security
    validates request
    then forwards to bank

    Structure
Client → Proxy → Real Object
Client → BankAccountProxy → RealBankAccount
*/

}
