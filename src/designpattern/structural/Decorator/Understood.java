package designpattern.structural.Decorator;

public class Understood {

    /*Idea

    Add behavior to an object dynamically at runtime without changing its original class.

    Instead of modifying a class, you wrap it with another class.

 Real-Life Analogy (Very Important)

    Think of coffee:

    Plain Coffee → ₹50
    Coffee + Milk → ₹70
    Coffee + Milk + Sugar → ₹80
    Coffee + Milk + Sugar + Chocolate → ₹100

You are not creating new classes for every combination.

You are decorating the base coffee step by step.

--
Component (interface)
        ↑
ConcreteComponent (basic object)
        ↑
Decorator (wrapper)
        ↑
ConcreteDecorators (add features)

*/
}
